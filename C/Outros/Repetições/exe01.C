#include <stdio.h>

int main(){
    int  i, divisor, dividendo, cont = 0;

    printf("Digite o dividendo e divisor: ");
    scanf("%d %d", &dividendo, &divisor);

    for (i = divisor; i <= dividendo; i += divisor){
            cont++;
    }
    printf("Quociente = %d\n", cont);
    return 0;
}
